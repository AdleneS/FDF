#include "libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int res;
	int neg;

	i = 0;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	neg = str[i];
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	if (neg == '-')
		res = -res;
	return (res);
}
