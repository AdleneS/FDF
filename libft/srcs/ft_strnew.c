#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	i = 0;
	str = malloc(size + 1);
	if (str)
		while (i < size)
		{
			str[i] = '\0';
			i++;
		}
	return (str);
}
