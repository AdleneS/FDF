#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = b;
	if (len == 0)
		return (b);
	while (i < len)
	{
		ptr[i] = c;
		i++;
	}
	return (b);
}
